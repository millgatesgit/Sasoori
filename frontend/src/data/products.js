/**
 * Filter and paginate products (mirrors backend API behaviour)
 */
export function filterProducts({ category, search, tag, page = 1, size = 12, sort = 'popular' } = {}) {
  let result = [...products];

  if (category) {
    result = result.filter(p => p.category_slug === category);
  }

  if (tag) {
    if (tag === 'new')     result = result.filter(p => p.tags.includes('new'));
    if (tag === 'offers')  result = result.filter(p => p.mrp_paise && p.mrp_paise > p.price_paise);
    if (tag === 'featured') result = result.filter(p => p.is_featured);
  }

  if (search) {
    const q = search.toLowerCase();
    result = result.filter(p =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.tags.some(t => t.includes(q))
    );
  }

  // Sort
  if (sort === 'price_asc')  result.sort((a, b) => a.price_paise - b.price_paise);
  if (sort === 'price_desc') result.sort((a, b) => b.price_paise - a.price_paise);
  if (sort === 'newest')     result.sort((a, b) => (b.tags.includes('new') ? 1 : 0) - (a.tags.includes('new') ? 1 : 0));

  const total = result.length;
  const offset = (page - 1) * size;
  const items  = result.slice(offset, offset + size);

  return { products: items, total, page, size, totalPages: Math.ceil(total / size) };
}

export function getProductBySlug(slug) {
  return products.find(p => p.slug === slug) || null;
}

export function getFeaturedProducts(limit = 6) {
  return products.filter(p => p.is_featured).slice(0, limit);
}
